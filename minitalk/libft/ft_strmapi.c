#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*buf;

	i = 0;
	if (!s || (!s && !f))
		return (ft_strdup(""));
	else if (!f)
		return (ft_strdup(s));
	buf = ft_strdup(s);
	if (!buf)
		return (buf = NULL);
	while (s[i])
	{
		buf[i] = (*f)(i, s[i]);
		i++;
	}
	return (buf);
}
